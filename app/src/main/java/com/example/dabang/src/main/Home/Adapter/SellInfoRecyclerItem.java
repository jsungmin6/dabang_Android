package com.example.dabang.src.main.Home.Adapter;

public class SellInfoRecyclerItem {

    private int sellInfoImg ;
    private String selling ;
    private String sellInfo ;
    private String sellInfoName ;

    public int getSellInfoImg() {
        return sellInfoImg;
    }

    public void setSellInfoImg(int sellInfoImg) {
        this.sellInfoImg = sellInfoImg;
    }

    public String getSelling() {
        return selling;
    }

    public void setSelling(String selling) {
        this.selling = selling;
    }

    public String getSellInfo() {
        return sellInfo;
    }

    public void setSellInfo(String sellInfo) {
        this.sellInfo = sellInfo;
    }

    public String getSellInfoName() {
        return sellInfoName;
    }

    public void setSellInfoName(String sellInfoName) {
        this.sellInfoName = sellInfoName;
    }
}
